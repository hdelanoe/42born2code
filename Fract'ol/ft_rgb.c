#include "fractol.h"

int	ft_rgb(int iteri, int max_iter)
{
	int	tab[max_iter];
	int i;

	i = 1;
	while (tab[i])
	{
		tab[i] = i;
		i++;
		if (i == 255)
			i = 0;
	}
	return (tab[iteri]);
}
