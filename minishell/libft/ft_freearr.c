#include "libft.h"
#include <stdlib.h>

void	ft_freearr(char ***arr)
{
	int i;

	if ((*arr))
	{
		i = -1;
		while ((*arr)[++i])
		{
			free((*arr)[i]);
			(*arr)[i] = NULL;
		}
		free((*arr));
		(*arr) = NULL;
	}
}
