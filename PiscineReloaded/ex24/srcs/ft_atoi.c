int		ft_atoi(char *str)
{
	int	i;
	int nb;
	int neg;

	i = 0;
	nb = 0;
	neg = 1;
	while (9 <= str[i] && str[i] <= 32)
		i++;
	if (str[i] == '-')
	{
		neg = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while ('0' <= str[i] && str[i] <= '9')
	{
		nb = (nb * 10) - '0' + str[i];
		i++;
	}
	return (nb * neg);
}
